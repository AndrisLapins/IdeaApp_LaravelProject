    public function add() {
        $fav = new Favorite;
        $fav->user_id = auth()->user()->id;
        $fav->post_id = $request->$post_id;
        $fav->save();

        $post = Post::find($post_id);
        $comments = Comment::where('post_id',$post_id)->get();
        return view('posts.show')->with(['post'=>$post,'comments'=>$comments, 'success'=>'Favorite Saved']);
    }

